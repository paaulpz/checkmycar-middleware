package com.paula.checkmc.model;

public class TipoCombustible {

    private Long id;
    private String nombre;

    public TipoCombustible() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}